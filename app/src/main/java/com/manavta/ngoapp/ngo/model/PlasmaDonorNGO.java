package com.manavta.ngoapp.ngo.model;

public class PlasmaDonorNGO {
    int plasmaid;
    String age, bloodgroup, city, dateofcovidnegative, dateofcovidpositive, gender, name, phonenumber, state, weight;


    public int getPlasmaid() {
        return plasmaid;
    }

    public void setPlasmaid(int plasmaid) {
        this.plasmaid = plasmaid;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getBloodgroup() {
        return bloodgroup;
    }

    public void setBloodgroup(String bloodgroup) {
        this.bloodgroup = bloodgroup;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getDateofcovidnegative() {
        return dateofcovidnegative;
    }

    public void setDateofcovidnegative(String dateofcovidnegative) {
        this.dateofcovidnegative = dateofcovidnegative;
    }

    public String getDateofcovidpositive() {
        return dateofcovidpositive;
    }

    public void setDateofcovidpositive(String dateofcovidpositive) {
        this.dateofcovidpositive = dateofcovidpositive;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
}
